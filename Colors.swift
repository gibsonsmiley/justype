import UIKit

extension UIColor {
    
    static func offWhiteColor() -> UIColor {
    return UIColor(red: 0.937, green: 0.937, blue: 0.937, alpha: 1.00)
    }
    
    static func offBlackColor() -> UIColor {
        return UIColor(red: 0.137, green: 0.157, blue: 0.173, alpha: 1.00)
    }
    
    static func notesYellow() -> UIColor {
        return UIColor(red: 1.000, green: 0.808, blue: 0.220, alpha: 1.00)
    }
}